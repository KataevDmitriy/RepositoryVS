using EFWebApi.Configuration;
using EFWebApi.Models;
using Microsoft.EntityFrameworkCore;

namespace EFWebApi
{
    public static class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();
            builder.Services.AddDbContext<EFWebApiContext>(opt =>
            {
                var config = builder.Configuration.GetSection("ConnectionStrings")
                    .Get<ConnectionConfiguration>();
                opt.UseSqlServer(config.EFWebApiConnection);
            });

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();

            app.MapControllers();

            app.Run();
        }
    }
}
